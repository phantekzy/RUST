// Control flow
// If Expressions
fn main() {
    let number = 6;
    if number % 4 == 0 {
        println!("number is divisible by 4");
    }
    if number % 3 == 0 {
        println!("number is divisble by 3");
    }
    if number % 2 == 0 {
        println!("number is divisible by 2");
    } else {
        println!("number is not divisible by the numbers 4,3 and 2");
    }
}
